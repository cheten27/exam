import java.io.*;

public class Solution{
   public static void main(String args[]){
       int numerator= 10;
       int denominator=20;

       if(numerator>denominator){
              System.out.println("numerator is greater than denominator");
       }
       else if(numerator==denominator){
              System.out.println("numerator is equal to denominator");
       }
       else{
              System.out.println("numerator is less than denominator");
       }
   }
}